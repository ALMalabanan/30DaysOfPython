# If Condition
a = 3
print("Value of a:", a)
if a > 0:
    print('A is a positive number')
else:
    print('A is a negative number')
# A is a positive number

# If Elif Else
a = 0
print('Value of A:', a)
if a > 0:
    print('A is a positive number')
elif a < 0:
    print('A is a negative number')
else:
    print('A is zero')
